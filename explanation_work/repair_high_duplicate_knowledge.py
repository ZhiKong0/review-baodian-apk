def answer_text(q: dict) -> str:
    answer = q.get("answer")
    if isinstance(answer, list):
        values = []
        for item in answer:
            values.append(str(item))
        return "、".join(values)
    if answer == "TRUE":
        return "T 正确"
    if answer == "FALSE":
        return "F 错误"
    option_map = {str(o.get("key")): str(o.get("text", "")).strip() for o in q.get("options", [])}
    if isinstance(answer, str) and answer in option_map:
        return option_map[answer]
    return str(answer)


def quick_reason(q: dict) -> str:
    quick = q.get("quickExplanation", "")
    match = re.search(r"本题理由：(.+?)(?:\n\n选项判断：|\n选项判断：|\Z)", quick, flags=re.S)
    if match:
        return " ".join(match.group(1).split())
    first = quick.strip().splitlines()[0] if quick.strip() else ""
    return first.replace("本题理由：", "").strip()


def build_detail(q: dict, focus: str, relation: str, trap: str) -> str:
    answer = answer_text(q)
    reason = quick_reason(q)
    stem = q.get("stem", "").strip()
    knowledge = q.get("knowledge", "")
    chapter = q.get("chapter", "")
    return f"""核心知识框架：
- 本题焦点：{focus}。
- 题干落点：{stem}
- 知识关系：{relation}
- 本题结论：答案落在 {answer}；直接理由是：{reason}

知识关系表：
| 关系节点 | 在本题中的角色 | 和相邻概念的边界 |
|---|---|---|
| {focus} | {relation} | 只要题干问到这个点，就不能被同章出现的相近名词带走。 |
| 所属知识点 | {knowledge} | 它解释的是本题这一层关系，不等于整章所有协议/设备都能套用。 |
| 正确答案 | {answer} | 能完整解释题干中的限定词：{reason} |

易混辨析表：
| 容易混的说法 | 为什么容易错 | 本题必须抓住的判断线 |
|---|---|---|
| {trap} | 只看关键词相似，没有看协议职责、层次或方向 | {relation} |
| 把上位概念当答案 | 上位概念能解释背景，但不一定回答题干问的那个空/选项 | 这里要落回“{focus}”，而不是泛泛背一整章。 |
| 把相邻名词互换 | 相邻名词经常同章出现，但作用对象不同 | 换成别的名词后，就不能解释：{reason} |

做题抓手：
- 看到这类题，直接把注意力放在“{focus}”这条关系上。
- 本题不是让你背整章，而是要判断“{relation}”是否正好回答题干。
- 如果另一个选项/名词无法解释“{reason}”，它就不是本题要的答案。"""


def main() -> None:
    questions = json.loads(QUESTIONS.read_text(encoding="utf-8"))
    changed = 0
    for q in questions:
        payload = FOCUS.get(q.get("label"))
        if not payload:
            continue
        q["knowledgeDetail"] = build_detail(q, *payload)
        q["explanation"] = f"【快速做题】\n{q.get('quickExplanation', '').strip()}\n\n【知识点详解】\n{q['knowledgeDetail']}"
        changed += 1
    QUESTIONS.write_text(json.dumps(questions, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"updated {changed} high-duplicate explanations")


if __name__ == "__main__":
    main()
