function item(title, summary = "") {
  return {
    title,
    summary,
    badge: "",
    points: summary ? [summary] : [],
    children: [],
  };
}

function group(title, summary, children, points = []) {
  return {
    title,
    summary,
    badge: "",
    points,
    children,
  };
}

function markdownFromNodes(title, nodes) {
  const lines = [
    `# ${title}`,
    "",
    "这版按“先定位问题 -> 再分清机制 -> 最后背判断句”的顺序整理，节点文字尽量写成能直接做题的短句。",
    "",
  ];
  function walk(node, depth) {
    const indent = "  ".repeat(depth);
    lines.push(`${indent}- ${node.title}`);
    for (const point of node.points || []) {
      if (point && point !== node.summary) {
        lines.push(`${indent}  - ${point}`);
      }
    }
    for (const child of node.children || []) {
      walk(child, depth + 1);
    }
  }
  for (const node of nodes) {
    walk(node, 0);
  }
  return lines.join("\n");
}

